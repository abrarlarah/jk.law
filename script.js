// ===== Event Listeners Setup =====
function setupEventListeners() {
    // Theme toggle
    const darkModeToggle = document.getElementById('dark-mode-toggle');
    if (darkModeToggle) {
        darkModeToggle.addEventListener('click', toggleDarkMode);
    }
    
    // High contrast toggle
    const highContrastToggle = document.getElementById('high-contrast-toggle');
    if (highContrastToggle) {
        highContrastToggle.addEventListener('click', toggleHighContrast);
    }
    
    // Font size controls
    const fontIncrease = document.getElementById('font-increase');
    const fontDecrease = document.getElementById('font-decrease');
    
    if (fontIncrease) {
        fontIncrease.addEventListener('click', increaseFontSize);
    }
    
    if (fontDecrease) {
        fontDecrease.addEventListener('click', decreaseFontSize);
    }
    
    // Language selector (top only)
    const languageSelectTop = document.getElementById('language-select-top');
    
    if (languageSelectTop) {
        languageSelectTop.addEventListener('change', changeLanguage);
    }
    
    // Search functionality
    const searchInput = document.getElementById('search-input');
    const searchBtn = document.getElementById('search-btn');
    
    if (searchInput) {
        searchInput.addEventListener('keypress', function(e) {
            if (e.key === 'Enter') {
                performSearch();
            }
        });
    }
    
    if (searchBtn) {
        searchBtn.addEventListener('click', performSearch);
    }
    
    // Window scroll events
    window.addEventListener('scroll', handleScroll);
    
    // Window resize events
    window.addEventListener('resize', handleResize);
}

// ===== Theme Management =====
function toggleDarkMode() {
    currentTheme = currentTheme === 'light' ? 'dark' : 'light';
    applyTheme(currentTheme);
    saveUserPreferences();
    
    // Update button icon
    const darkModeToggle = document.getElementById('dark-mode-toggle');
    if (darkModeToggle) {
        const icon = darkModeToggle.querySelector('i');
        if (currentTheme === 'dark') {
            icon.className = 'fas fa-sun';
            darkModeToggle.title = 'Switch to Light Mode';
        } else {
            icon.className = 'fas fa-moon';
            darkModeToggle.title = 'Switch to Dark Mode';
        }
    }
}

function toggleHighContrast() {
    const body = document.body;
    const currentTheme = body.getAttribute('data-theme');
    
    if (currentTheme === 'high-contrast') {
        body.removeAttribute('data-theme');
        applyTheme('light');
    } else {
        body.setAttribute('data-theme', 'high-contrast');
    }
    
    saveUserPreferences();
}

function applyTheme(theme) {
    const body = document.body;
    
    if (theme === 'dark') {
        body.setAttribute('data-theme', 'dark');
    } else if (theme === 'high-contrast') {
        body.setAttribute('data-theme', 'high-contrast');
    } else {
        body.removeAttribute('data-theme');
    }
    
    currentTheme = theme;
}

// ===== Font Size Management =====
function increaseFontSize() {
    fontSize = Math.min(fontSize + 2, 24);
    applyFontSize();
    saveUserPreferences();
}

function decreaseFontSize() {
    fontSize = Math.max(fontSize - 2, 12);
    applyFontSize();
    saveUserPreferences();
}

function applyFontSize() {
    document.documentElement.style.fontSize = fontSize + 'px';
}

// ===== Language Management =====
function changeLanguage(event) {
    const selectedLanguage = event.target.value;
    currentLanguage = selectedLanguage;
    applyLanguage(selectedLanguage);
    saveUserPreferences();
    
    // Language selector is already updated by the event
}

function applyLanguage(language) {
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = translations[language] && translations[language][key];
        
        if (translation) {
            if (element.tagName === 'INPUT' && element.type === 'text') {
                element.placeholder = translation;
            } else {
                element.innerHTML = translation;
            }
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = language;
}

// ===== Counter Animation =====
function initializeCounters() {
    const counters = document.querySelectorAll('.stat-number');
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                animateCounter(entry.target);
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });
    
    counters.forEach(counter => {
        observer.observe(counter);
    });
}

function animateCounter(element) {
    const target = parseInt(element.getAttribute('data-count'));
    const duration = 2000;
    const increment = target / (duration / 16);
    let current = 0;
    
    const timer = setInterval(() => {
        current += increment;
        if (current >= target) {
            current = target;
            clearInterval(timer);
        }
        element.textContent = Math.floor(current);
    }, 16);
}

